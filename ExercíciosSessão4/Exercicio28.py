"""
Faça a leitura de tres valores e apresente
como resultado a soma dos quadrados dos tres valores lidos
"""

valor1 = int(input('Digite o primeiro número: '))
valor2 = int(input('Digite o sendo número: '))
valor3 = int(input('Digite o terceiro número: '))

soma = valor1 + valor2 + valor3

print(soma.__pow__(2))

